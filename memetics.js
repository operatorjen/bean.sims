export const getThought = s => {
  if (s.conditions.anxiety < .2) return ''

  const d = D.reduce((a, b) => s.traits[a] >= s.traits[b] ? a : b)

  const v = s.traits[d],
        i = T.findIndex(t => v >= t),
        j = i < E[d].length ? i : E[d].length - 1

  return E[d][j][ Math.random() * E[d][j].length | 0 ]
}